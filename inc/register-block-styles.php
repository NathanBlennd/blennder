		array(
			'name'  => 'blennder-flat-button',
			'label' => __( 'Flat button', 'blennder' ),
		)
	);

	register_block_style( // phpcs:ignore WPThemeReview.PluginTerritory.ForbiddenFunctions.editor_blocks_register_block_style
		'core/button',
		array(
			'name'  => 'blennder-button-shadow',
			'label' => __( 'Button with shadow', 'blennder' ),
		)
	);

	register_block_style( // phpcs:ignore WPThemeReview.PluginTerritory.ForbiddenFunctions.editor_blocks_register_block_style
		'core/navigation',
		array(
			'name'  => 'blennder-navigation-button',
			'label' => __( 'Button style', 'blennder' ),
		)
	);

	register_block_style( // phpcs:ignore WPThemeReview.PluginTerritory.ForbiddenFunctions.editor_blocks_register_block_style
		'core/navigation',
		array(
			'name'  => 'blennder-navigation-button-shadow',
			'label' => __( 'Button with shadow', 'blennder' ),
		)
	);

	register_block_style( // phpcs:ignore WPThemeReview.PluginTerritory.ForbiddenFunctions.editor_blocks_register_block_style
		'core/list',
		array(
			'name'  => 'blennder-list-underline',
			'label' => __( 'Underlined list items', 'blennder' ),
